public class BinaryTest{
	public static void main(String[] args){

		//n1  Binary
		int n1 = 0b1010;
		//n2  Decimal
		int n2 = 1010;
		//n3  Octal
		int n3 = 01010;
		//n4  Hexadecimal
		int n4 = 0X10101;
		System.out.println(n1);
		System.out.println(n2);
		System.out.println(n3);
		System.out.println(n4);
	}
}